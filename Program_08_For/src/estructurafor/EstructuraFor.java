public class EstructuraFor { // Inicio De la clase EstructuraFor
    
    public static void main(String[] args) { // Inicio de la funcion Principal
        
        for(int Contador = 0; Contador <=10 ; Contador++){ // Inicio del bucle For
            System.out.println("El Valor del Contador es de: "+Contador);
        } // Fin del Bucle For
    } // Inicio de la funcion Principal    
}// Fin De la clase EstructuraFor
