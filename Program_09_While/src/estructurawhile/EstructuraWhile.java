public class EstructuraWhile { // Inicio De la clase EstructuraWhile
    
    public static void main(String[] args) { // Inicio de la funcion Principal
        
        int Contador = 0;
        while(Contador <=10){ // Inicio del bucle While
             System.out.println("El Valor del Contador es de: "+Contador);
             Contador++;
        } // Fin del Bucle While
    } // Inicio de la funcion Principal
} // Fin De la clase EstructuraWhile
