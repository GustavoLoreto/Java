public class Arrays2D { // Inicio De la clase Arrays2D
    
    public static void main(String[] args) { // Inicio de la funcion principal
        
        String Nombres[][] = {{"PEDRO","JUAN"},{"MARIA","ADRIANA"}};
        
        // Nombres[Fil][Col]
        
        System.out.println(Nombres [0][0]);
        System.out.println(Nombres [0][1]);
        System.out.println(Nombres [1][0]);
        System.out.println(Nombres [1][1]);
        
    } // Fin de la funcion principal
} // Fin De la clase Arrays2D
