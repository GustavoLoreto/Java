public class EstructuraDoWhile { // Inicio De la clase EstructuraDoWhile
    
    public static void main(String[] args) { // Inicio de la funcion Principal
        
        int Contador = 0;
        do{ // Inicio del bucle  Do While
             System.out.println("El Valor del Contador es de: "+Contador);
             Contador++;
        }while(Contador <=10); // Fin del Bucle Do While
    } // Inicio de la funcion Principal
} // Fin De la clase EstructuraDoWhile