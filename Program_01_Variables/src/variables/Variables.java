public class Variables { // Inicio De la clase Variables
    
    public static void main(String[] args) { // Inicio de la funcion principal
        
        // Declaracion e inicializacion de variables
        int PrimeraVariable = 3;             // Variabe entera
        double SegundaVariable = 3.2;        // Variable de del presicion
        String TerceraVariable = "Cadena";   // Variable Tipo Cadena
        boolean CuartaVariable = true ;      // Variable Tipo Booleana
        
        // Devolver Valor
        System.out.println(PrimeraVariable);
        System.out.println(SegundaVariable);
        System.out.println(TerceraVariable);
        System.out.println(CuartaVariable);
        
    } // Fin de la funcion principal
} // Fin De la clase Variables
